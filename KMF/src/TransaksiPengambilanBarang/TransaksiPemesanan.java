    private void addItemJenisPaket(){
        cmbJenis_paket.removeAll();

        try {
           
            connection.stat = connection.conn.createStatement();
            String query = "select jns_nama from JenisPaket";
            connection.result = connection.stat.executeQuery(query);
            connection.result.next();
            do {
                cmbJenis_paket.addItem(connection.result.getString("jns_nama"));
            }while (connection.result.next());
            connection.stat.close();
            connection.result.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal111!\n" + e.toString());
        }
    }
    private void addItemKota(){
        cmbKotaKabupaten.removeAll();
        cmbKota_pickup.removeAll();
        try {
            
            connection.stat = connection.conn.createStatement();
            String query = "select nama_kota from KotaKabupaten";
            connection.result = connection.stat.executeQuery(query);

            while (connection.result.next()) {
                cmbKotaKabupaten.addItem(connection.result.getString("nama_kota"));
                cmbKota_pickup.addItem(connection.result.getString("nama_kota"));
            }
            connection.stat.close();
            connection.result.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal111!\n" + e.toString());
        }
    }
