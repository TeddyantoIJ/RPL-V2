            connection.pstat.setString(18, txtid_cs);
            connection.pstat.setString(19, getIDKantor());
            

            connection.pstat.executeUpdate();
            connection.pstat.close();
            JOptionPane.showMessageDialog(this,
                    "Data tersimpan");
            
