    public void loadData() {
        // menghapus seluruh data ditamp
        model.getDataVector().removeAllElements();
        
        // memberi tahu data telah kosong
        model.fireTableDataChanged();
        
        try {
           
            connection.stat = connection.conn.createStatement();
            String query = "SELECT * FROM Departement";
            connection.result = connection.stat.executeQuery(query);
            
            // lakukan perbaris data
            while (connection.result.next()) {
                 Object[] obj = new Object[2];
                obj[0] = connection.result.getString("id_dept");
                obj[1] = connection.result.getString("nama_dept");
                model.addRow(obj);
            }
            connection.stat.close();
            connection.result.close();
        } catch (Exception e) {
            System.out.println("Terjadi error saat load data departement: " + e);
        }
    }
    
