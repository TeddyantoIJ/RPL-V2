    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        ID = txtid.getText();
        nama = txtNama.getText();

        try {

            String query = "UPDATE Departement SET nama_dept=? WHERE id_dept=?";
            connection.pstat = connection.conn.prepareStatement(query);
            connection.pstat.setString(1, nama);
            connection.pstat.setString(2, ID);

            connection.pstat.executeUpdate();
            connection.result.close();

            loadData();
            JOptionPane.showMessageDialog(this, "Ubah data departement berhasil");
            clear();
        }
        catch (Exception e) {
            System.out.println("Terjadi error: " + e);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

