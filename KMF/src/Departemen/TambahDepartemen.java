    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        ID = txtid.getText();
        nama = txtNama.getText();
        pass = txtpass.getText();

        if(ID.equals("")||nama.equals("")){
            JOptionPane.showMessageDialog(this, "nama tidak boleh kosong!");
        }
        else
        {
            try
            {
                String query = "INSERT INTO Departement VALUES(?,?,?)";
                connection.pstat = connection.conn.prepareStatement(query);
                connection.pstat.setString(1, ID);
                connection.pstat.setString(2, nama);
                connection.pstat.setString(3, pass);
                
                connection.pstat.executeUpdate();//insert ke database
                connection.pstat.close();//menutup koneksi db

                JOptionPane.showMessageDialog(this, "Insert data Departement berhasil");
                clear();
            }
            catch (Exception ex) {
                System.out.println("Terjadi error pada saat insert data departemen: " + ex);
            }
        }
        clear();
        txtid.setText("DPT-"+getLastID());
    }//GEN-LAST:event_btnSimpanActionPerformed
    
