    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        id = txtId.getText();
        nama = txtNama.getText();
        alamat = txtAlamat.getText();
        String tanggal = formatter.format(txtTgl.getDate());
        //TglLahir = TglLahir;
        noTelp = txtTlp.getText();
        email = txtEmail.getText();
        username = txtUname.getText();
        password = txtPass.getText();

        if(id.equals("")||nama.equals("")||alamat.equals("")||tanggal.equals("")||noTelp.equals("")||email.equals("")||username.equals("")||password.equals("")){
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
        }
        else
        {
            try
            {
                String query = "INSERT INTO CustomerService VALUES(?,?)";
                connection.pstat = connection.conn.prepareStatement(query);
                connection.pstat.setString(1, id);
                connection.pstat.setString(2, nama);
                connection.pstat.setString(3, alamat);
                connection.pstat.setString(4, tanggal);
                connection.pstat.setString(5, noTelp);
                connection.pstat.setString(6, email);
                connection.pstat.setString(7, username);
                connection.pstat.setString(8, password);
                
                connection.pstat.executeUpdate();//insert ke database
                connection.pstat.close();//menutup koneksi db

                JOptionPane.showMessageDialog(this, "Insert data Customer Service berhasil");
                clear();
            }
            catch (Exception ex) {
                System.out.println("Terjadi error pada saat insert data Customer Service: " + ex);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

