    private void addItemDepartemen(){
        cbDept.removeAll();
        try {
            
            connection.stat = connection.conn.createStatement();
            String query = "SELECT nama_dept FROM Departement";
            connection.result = connection.stat.executeQuery(query);
            connection.result.next();
            do{
                
                cbDept.addItem(connection.result.getString("nama_dept"));
            }while(connection.result.next());
            
            connection.stat.close();
            connection.result.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tambah item Departemen gagal!\n" + e.toString());
        }
    }
    
    private String getLastID(){
        try{
            connection.stat = connection.conn.createStatement();
                String query = "select count(id_staff) Total from Staff";
                connection.result = connection.stat.executeQuery(query);
                int count = 0;
                while (connection.result.next()) {
                    
                    count = Integer.parseInt(connection.result.getString("total"));
                    }
                connection.stat.close();
                connection.result.close();
                
                
                count++;
                
                if(Integer.toString(count).length() == 1)
            {
                return "000" + count;
            }
            else if (Integer.toString(count).length() == 2)
            {
                return "00" + count;
            }
            else if (Integer.toString(count).length() == 3)
            {
                return "0" + count;
            }
            else
            {
                return Integer.toString(count);
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Generate Id gagal!\n" + ex.toString());
            return null;
        }
    }
